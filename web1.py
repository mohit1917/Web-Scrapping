import pandas as pd
from bs4 import BeautifulSoup
import requests, openpyxl
import xlsxwriter
from urllib.request import urlopen as uReq

Product_name =[]
Prices = []
Description = []
Reviews = []


excel = openpyxl.Workbook()
print(excel.sheetnames)
sheet = excel.active
sheet.title = 'mobile phones'
print(excel.sheetnames)
sheet.append(['Product_name','Prices','Description','Reviews'])


for i in range(2,12):

    url = "https://www.flipkart.com/search?q=mobile+under+50000&otracker=search&otracker1=search&marketplace=FLIPKART&as-show=on&as=off&page="+str(i)


    r = requests.get(url)
    print(r)

    soup = BeautifulSoup(r.text, "lxml")

    box = soup.find("div",class_ = "_1YokD2 _3Mn1Gg")

    names = soup.find_all("div",class_ = "_4rR01T")
    for i in names:
        name = i.text
        Product_name.append(name)

    #print(len(Product_name))

    prices = box.find_all("div", class_="_30jeq3 _1_WHN1")

    for i in prices:
        name = i.text
        Prices.append(name)

    #print(len(Prices))

    desc = box.find_all("ul", class_="_1xgFaf")

    for i in desc:
        name = i.text
        Description.append(name)

   # print(len(Description))

    reviews = box.find_all("div", class_="_3LWZlK")

    for i in reviews:
        name = i.text
        Reviews.append(name)

    #print(len(Reviews))


df = pd.DataFrame({"Product Name": Product_name, "Prices": Prices, "Description": Description, "Reviews": Reviews})
df = pd.DataFrame.from_dict(df, orient='index')
df = df.transpose()
print(df)


df.to_csv("flipkart_mobiles_under_50000.csv")
excel.save('flipkart_mobile_phones_50000.xlsx')





